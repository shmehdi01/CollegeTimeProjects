    public void equalTo()
    {
      try {
            Sec = Double.parseDouble(Display.getText()) ;
        }catch(Exception e) {}
        
        if(operator == "+")
        {
          Result = First + Sec;
          Display.setText(""+Result);
          
        }
        if(operator == "-")
        {
          Result = First - Sec;
          Display.setText(""+Result);
          
        }
        if(operator == "*")
        {
          Result = First * Sec;
          Display.setText(""+Result);
          
        }
        if(operator == "/")
        {
          Result = First / Sec;
          Display.setText(""+Result);
          
          
        }
    }
    
    public void Get()
    {
        First = Double.parseDouble(store.getText()) ;
        store.setText("");
       
        if(operator == "+")
        {
           Result = Result + First;
          
        }
        if(operator == "-")
        {
           min = (First - min);
           Result = (-1)*(min);
          
        }
        if(operator == "*")
        {
           res = res* First;
           Result = res;
          
        }
        if(operator == "/")
        {
          res =  First/res;
          Result = (1/res);
          
        }
    }
    
