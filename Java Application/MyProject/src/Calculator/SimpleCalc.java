    public void backSp()
    {
        String backspace = null;
        if(Display.getText().length() > 0)
        {
          StringBuilder sb = new StringBuilder(Display.getText());
          sb.deleteCharAt(Display.getText().length() - 1);
          backspace = sb.toString();
          Display.setText(backspace);
        }
    }
    
    
    
  

